#include "libft.h"

char	*ft_itoa(int n)
{
	char	*str;

	str = ft_strnew (20);
	if (n >= 0 && str)
	{
		*--str = '0' + (n % 10);
		n /= 10;
		while (n != 0)
		{
			*--str = '0' + (n % 10);
			n /= 10;
		}
	}
	else if (str)
	{
		*--str = '0' - (n % 10);
		n /= 10;
		while (n != 0)
		{
			*--str = '0' - (n % 10);
			n /= 10;
		}
		*--str = '-';
	}
	return (str);
}
