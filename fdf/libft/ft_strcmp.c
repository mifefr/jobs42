#include <string.h>

int		ft_strcmp(const char *s1, const char *s2)
{
	int		counter;

	if (s1 == NULL && s2 == NULL)
		return (0);
	if (s1 == NULL)
		return (-1);
	if (s2 == NULL)
		return (1);
	counter = 0;
	while (s1[counter] == s2[counter] && s1[counter] != '\0')
		counter++;
	return ((s1[counter] - s2[counter]));
}
